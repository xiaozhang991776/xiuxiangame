def mat_obj(t):
    return {'id': t[0], 'name': t[1], 'icon': t[2], 'quality': t[3], 'price': t[4], 'desc': t[5]}

def pill_obj(t):
    o = {'id': t[0], 'name': t[1], 'icon': t[2], 'quality': t[3], 'desc': t[4],
           'effect': {'type': t[5][0], 'value': t[5][1]}, 'price': t[6]}
    if len(t[5]) > 2:
        o['effect']['turns'] = t[5][2]
    o['craft'] = pill_craft(t[3], t[6])
    return o

def equip_obj(t):
    o = {'id': t[0], 'type': t[1], 'name': t[2], 'icon': t[3], 'quality': t[4],
           'atk': t[5], 'def': t[6], 'hp': t[7], 'ling': t[8], 'crit': t[9]}
    if t[1] == 'fabao':
        # 法宝元组：skill 插在 price/desc 之前
        o['skill'] = t[10]
        o['price'] = t[11]
        o['desc'] = t[12]
    else:
        o['price'] = t[10]
        o['desc'] = t[11]
        o['craft'] = equip_craft(t[1], t[4], t[10])
    return o

def gongfa_obj(t):
    return {'id': t[0], 'name': t[1], 'icon': t[2], 'xiuBonus': t[3],
            'realmReq': t[4], 'price': t[5], 'desc': t[6]}

def pet_obj(t):
    return {'id': t[0], 'name': t[1], 'icon': t[2], 'elem': t[3], 'realmReq': t[4],
            'atk': t[5], 'def': t[6], 'hp': t[7], 'skill': t[8],
            'price': t[9], 'desc': t[10]}

